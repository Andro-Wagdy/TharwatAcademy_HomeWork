/*
How do you concatenate strings in Dart? Write an example using two variables:
firstName and lastName.
*/

void main() {
  String firstName;
  String lastName;
  firstName = 'Andro';
  lastName = 'Wagdy';
  print('My first name is $firstName and my last name is $lastName');
  print('My whole name is $firstName $lastName');
  print('$firstName' + ' ' + '$lastName');
}
